#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int ended;

	ended = 0;
	while (!ended)
	{
		if (*s == '\0')
			ended = 1;
		if (*s == c)
			return ((char *)s);
		s++;
	}
	return (NULL);
}
