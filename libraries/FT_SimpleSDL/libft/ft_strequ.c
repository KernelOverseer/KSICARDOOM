#include "libft.h"

int	ft_strequ(const char *s1, const char *s2)
{
	int ended;

	ended = 0;
	if (!s1 || !s2)
		return (0);
	while (!ended)
	{
		if (*s1 == '\0' || *s2 == '\0')
			ended = 1;
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	return (1);
}
