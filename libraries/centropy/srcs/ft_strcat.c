#include "centropy.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t len1;
	size_t i;

	i = 0;
	len1 = ft_strlen(s1);
	while (s2[i])
	{
		*(s1 + len1) = s2[i];
		i++;
		len1++;
	}
	*(s1 + len1) = '\0';
	return (s1);
}
