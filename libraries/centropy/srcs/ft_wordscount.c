#include "centropy.h"

int	ft_wordscount(char const *s, char c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c && s[i] != '\0')
		{
			j++;
			while (s[i] != c && s[i] != '\0')
			{
				i++;
			}
		}
		else
		{
			i++;
		}
	}
	return (j);
}
